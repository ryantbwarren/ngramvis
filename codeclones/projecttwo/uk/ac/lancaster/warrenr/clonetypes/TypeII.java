public class TypeII {
    public void renamedMethodSignature(int max) {
        for (int i = 0; i < max; i++) {
            System.out.println(i);
        }
    }

    public void renamingIdentifiers(String message) {
        long errorCode = 1L;
        System.out.println(message + errorCode);
    }
}